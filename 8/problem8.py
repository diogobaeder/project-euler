class Problem8(object):
    def greatest_product_in(self, number):
        largest_so_far = 0
        remaining_digits = number

        while remaining_digits >= 10000:
            product_digits = remaining_digits % 100000
            product = 1
            while product_digits > 1:
                current_digit = product_digits % 10
                product_digits //= 10
                product *= current_digit
            remaining_digits //= 10
            if product > largest_so_far:
                largest_so_far = product
        return largest_so_far

